//E (AND) Só é verdadeiro de ambas as partes forem true - Operador &&
true && true // = true
3 > 2 && 8 !== 9 // = true

//OU (OR) Só é falso se ambas as partes forem falsas - Operador ||
7 !== 'Victor' || 6 < 2 // = true

//Não (NOT) Inverte o resultado (de verdadeiro para falso e vice-versa) - Operador !
!10 < 8 // = true